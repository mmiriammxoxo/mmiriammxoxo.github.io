////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
    var outputNumber = 0;
    for (var i = 0; i < string.length; i++){
        if (string.charAt(i) === "b" || string.charAt(i) === "B" ) {
            outputNumber = outputNumber +1;
        } 
    } return outputNumber;
}



